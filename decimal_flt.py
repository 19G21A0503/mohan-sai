
#Float
#Implicit
a = 50.3
print(a)
print(type(a))

#Explicit
a = float(10.01)
print(a)
print(type(a))

#Variable AAnnotation
a: float = 1.3
print(a)
print(type(a))

#Binary
a = float(0b1010)
print(a)
print(type(a))

#Octal
a = float(0o1010)
print(a)
print(type(a))

#HexaDecimal
a = float(0x1010)
print(a)
print(type(a))

#Boolean
a = float(True)
print(a)
print(type(a))

#2 Boolean
a = float(False)
print(a)
print(type(a))