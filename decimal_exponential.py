
#Exponential
#Implicit
a = 1e2
print(a)
print(type(a))

#Explicit
a = float(1545436356536546657386486876543234567)
print(a)
print(type(a))

#Variable Annotation
a: float =1.3e2
print(a)
print(type(a))

#Binary
a = float(0b1010)
print(a)
print(type(a))

#Octal
a = float(0o1010)
print(a)
print(type(a))

#HexaDecimal
a = float(0xface)
print(a)
print(type(a))

#Boolean
a = float(True)
print(a)
print(type(a))

a = float(False)
print(a)
print(type(a))